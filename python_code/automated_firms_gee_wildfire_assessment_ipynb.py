target_months = [1, 3, 6, 9, 12]

for m in target_months:
    # Calculate the time window for the current month
    start_date_dt = end_dt + timedelta(days=1 + (m - 1) * 30)
    end_date_exclusive_dt = start_date_dt + timedelta(days=30)
    end_date_inclusive_dt = end_date_exclusive_dt - timedelta(days=1)

    s_str = start_date_dt.strftime("%Y-%m-%d")
    e_str = end_date_exclusive_dt.strftime("%Y-%m-%d")

    # Extract median composite image for the current month
    monthly_img, monthly_count = get_median_composite(
        s_str, e_str, allow_empty=True
    )
    if monthly_img is None:
        print(f"⚠️ NDVI map month {m} skipped: no qualifying Sentinel-2 images.")
        continue

    # Calculate monthly land NDVI
    raw_ndvi = get_ndvi(monthly_img)

    # [Core Spatial Filtering]: Simultaneously update dual water mask and fire burned area mask (burned_mask)
    # This ensures that only burned pixels on land and with dNBR > 0.1 are calculated and colored
    clean_ndvi_burned_only = (raw_ndvi
                              .clipToCollection(land_boundaries) # Clip oceans
                              .updateMask(inland_water_mask)     # Mask out inland lakes
                              .updateMask(burned_mask))          # Only extract burn scar

    # Add the spatial layer for this month to the map
    layer_name = (
        f"Post-Fire Month {m} Burned Area NDVI "
        f"({s_str} to {end_date_inclusive_dt.strftime('%Y-%m-%d')})"
    )
    # By default, display the 1st month
    is_shown = True if m in [1] else False
    Map3.addLayer(clean_ndvi_burned_only, vis_ndvi, layer_name, is_shown)

