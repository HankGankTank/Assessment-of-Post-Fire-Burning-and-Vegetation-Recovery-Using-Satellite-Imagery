# Note: To prevent GEE timeout due to excessively large calculation areas, the scale parameter is set to 100m or 200m
pre_ndvi_val = pre_ndvi_burned_only.reduceRegion(
    reducer=ee.Reducer.mean(),
    geometry=roi,
    scale=100,
    maxPixels=1e10
).get('NDVI').getInfo()

if pre_ndvi_val is None:
    print("⚠️ No valid pre-fire NDVI baseline pixels were found in the burned area.")
else:
    print(f"🔥 Average pre-fire NDVI in the burned area: {pre_ndvi_val:.4f}")

# ==========================================
# 2. Loop to extract average NDVI for 12 months after the fire
# ==========================================
months_to_track = 12
dates = []
ndvi_values = []

print("📊 Extracting 12-month post-fire NDVI time series, cloud computing will take some time...")

for i in range(months_to_track):
    # Dynamically calculate the start and end dates for each month
    period_start_dt = end_dt + timedelta(days=1 + i * 30)
    period_end_exclusive_dt = period_start_dt + timedelta(days=30)
    period_end_inclusive_dt = period_end_exclusive_dt - timedelta(days=1)

    p_start_str = period_start_dt.strftime("%Y-%m-%d")
    p_end_str = period_end_exclusive_dt.strftime("%Y-%m-%d")

    # Retrieve median image for the current month
    monthly_img, monthly_count = get_median_composite(
        p_start_str, p_end_str, allow_empty=True
    )
    if monthly_img is None:
        print(f"⚠️ Month {i+1} skipped: no qualifying Sentinel-2 images.")
        continue

    # Calculate monthly NDVI and mask it with burned_mask, retaining only burned pixels
    monthly_ndvi = get_ndvi(monthly_img).updateMask(burned_mask)

    # Calculate the mean for this area
    try:
        mean_dict = monthly_ndvi.reduceRegion(
            reducer=ee.Reducer.mean(),
            geometry=roi,
            scale=100,
            maxPixels=1e10
        )
        val = mean_dict.get('NDVI').getInfo()

        if val is not None:
            dates.append(p_start_str)
            ndvi_values.append(val)
            print(
                f"✅ Month {i+1} ({p_start_str} to "
                f"{period_end_inclusive_dt.strftime('%Y-%m-%d')}): NDVI = {val:.4f}"
            )
        else:
            print(f"⚠️ Month {i+1} ({p_start_str}): No valid data (possibly fully cloud-covered this month)")
    except Exception as e:
        print(f"❌ Month {i+1} ({p_start_str}) extraction failed: {e}")

